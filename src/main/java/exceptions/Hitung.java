package exceptions;

public class Hitung {

    int numA, numB;

    public Hitung(int a, int b) {
        numA = a;
        numB = b;
    }

    public int getNumA() {
        return numA;
    }

    public void setNumA(int numA) {
        this.numA = numA;
    }

    public int getNumB() {
        return numB;
    }

    public void setNumB(int numB) {
        this.numB = numB;
    }
}
